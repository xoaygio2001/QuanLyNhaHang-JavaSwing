package modal;

/**
 *
 * @author DELL
 */
public class Food {
    int id;
    String name;
    int price;
    String type;
    
    int quantity;
    int total;

    public Food() {
    }

    public Food(int id, String name, int price, String type) {
        this.id = id;
        this.name = name;
        this.price = price;
        this.type = type;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }
    
    
    

    @Override
    public String toString() {
        return "Food{" + "id=" + id + ", name=" + name + ", price=" + price + ", type=" + type + '}';
    }
    
    
    
}
